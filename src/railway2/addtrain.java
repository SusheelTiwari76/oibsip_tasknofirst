     public void cancelTrain(int price4) {
    String input = JOptionPane.showInputDialog(this, "Please enter your PNR number to cancel or view train details:");

    try {
        if (input != null && !input.isEmpty()) {
            int inputPrice = Integer.parseInt(input);

            if (inputPrice == price4) {
                // Fetch train details based on PNR number
                String query = "SELECT * FROM traindetails WHERE price = ?";
                pst = con.prepareStatement(query);
                pst.setInt(1, price4);
                ResultSet rs = pst.executeQuery();

                // Display train details if found
                if (rs.next()) {
                    String tno = rs.getString("tno");
                    String trainname = rs.getString("tname");
                    String startplace = rs.getString("startplace");
                    String destinationplace = rs.getString("destinationplace");

                    String details = "Train No: " + tno + "\n" +
                                     "Train Name: " + trainname + "\n" +
                                     "Start Place: " + startplace + "\n" +
                                     "Destination Place: " + destinationplace;

                    // Ask for cancellation confirmation
                    int option = JOptionPane.showConfirmDialog(this, details + "\n\nDo you want to cancel this train?", "Train Cancellation", JOptionPane.YES_NO_OPTION);
                    if (option == JOptionPane.YES_OPTION) {
                        // Perform cancellation
                        pst = con.prepareStatement("DELETE FROM traindetails WHERE price = ?");
                        pst.setInt(1, price4);
                        int affectedRows = pst.executeUpdate();

                        if (affectedRows > 0) {
                            JOptionPane.showMessageDialog(this, "Train successfully canceled!");
                        } else {
                            JOptionPane.showMessageDialog(this, "Failed to cancel the train.", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No train details found for the entered PNR.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect price. Train details cannot be canceled.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter a valid number.");
        }
    } catch (NumberFormatException | SQLException e) {
        JOptionPane.showMessageDialog(this, "Error retrieving train details.");
    }
}
   
