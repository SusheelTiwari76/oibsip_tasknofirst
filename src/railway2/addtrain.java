    public int generateRandomPrice() {
    int minPrice2 = 100; // Define the minimum price
    int maxPrice2 = 1000; // Define the maximum price

    return (int) (Math.random() * (maxPrice2 - minPrice2 + 1)) + minPrice2;
}

